#Escap sequence
print("Hello \"world\" ") #\ for escape sequence
print("Hello \'world\' ") #\' for single quote
print("Hello \nworld") #n for new line
print("Hello \nworld") #t for tab
print("Hello \tworld") #tab will add space into both words
print("Hello \\world") #\\ for backslash
print("Hello \rworld") #r for carriage return
print("Hello \bworld") #backspace will remove the last character
print("Hello \fworld") #formfeed