#Arithmatic operators
print(5+6)
print(5-6)
print(5*6)
print(5/2)
print(5//2) #5 divided by 2 equals 2.5, but the floor division rounds it down to 2.

#modulus operator ---> calculates the remainder after division
print(5%2) #The modulo operation calculates the remainder after division of one number by another , 5 divided by 2 equals 2 with a remainder of 1 ,Therefore, (5%2) evaluates to 1.
print(5**2) #exponentiation operator like square of 5

#Assignment operators
x=5
x+=7
print(x)

#Comparison operators
i=5
print(i==5)
print(i!=5)
print(i>=5)
print(i<=5)

#Logical operators
a=True
b=False
print(a and b)
print(a or b)

#Identity operators
print(a is b)
print(a is not b)

#Membership operators
list=[3,3,2,2,39,33,35,32]
print(324 not in list)
print(3 in list)

#Bitwise operators
#0-00
#1-01
#2-10
#3-11
print(0 & 1)
print(0 | 1)